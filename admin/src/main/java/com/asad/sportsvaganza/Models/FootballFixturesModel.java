package com.asad.sportsvaganza.Models;


public class FootballFixturesModel {
    public String teamA;
    public String teamB;
    public String time;
    public String date;

    public FootballFixturesModel(String teamA, String teamB, String time, String date){
        this.teamA = teamA;
        this.teamB = teamB;
        this.time = time;
        this.date = date;
    }
}
